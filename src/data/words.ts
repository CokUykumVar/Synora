// Combine all word categories
const allWords: WordData[] = [...travelWords, ...foodWords, ...businessWords, ...technologyWords];

// Get words for a specific language pair
export function getWordsForLanguagePair(
  learningLanguageCode: string,
  nativeLanguageCode: string
): Array<{
  id: string;
  word: string;
  translation: string;
  pronunciation: string;
  example: string;
  exampleTranslation: string;
  category: string;
}> {
  return allWords
    .filter(
      (wordData) =>
        wordData.translations[learningLanguageCode] &&
        wordData.translations[nativeLanguageCode]
    )
    .map((wordData) => {
      const learningWord = wordData.translations[learningLanguageCode];
      const nativeWord = wordData.translations[nativeLanguageCode];

      return {
        id: wordData.id,
        word: learningWord.word,
        translation: nativeWord.word,
        pronunciation: learningWord.pronunciation || '',
        example: learningWord.example || '',
        exampleTranslation: nativeWord.example || '',
        category: wordData.category,
      };
    });
}

// Get words for a specific category
export function getWordsByCategory(
  category: string,
  learningLanguageCode: string,
  nativeLanguageCode: string
): Array<{
  id: string;
  word: string;
  translation: string;
  pronunciation: string;
  example: string;
  exampleTranslation: string;
  category: string;
}> {
  return allWords
    .filter(
      (wordData) =>
        wordData.category === category &&
        wordData.translations[learningLanguageCode] &&
        wordData.translations[nativeLanguageCode]
    )
    .map((wordData) => {
      const learningWord = wordData.translations[learningLanguageCode];
      const nativeWord = wordData.translations[nativeLanguageCode];

      return {
        id: wordData.id,
        word: learningWord.word,
        translation: nativeWord.word,
        pronunciation: learningWord.pronunciation || '',
        example: learningWord.example || '',
        exampleTranslation: nativeWord.example || '',
        category: wordData.category,
      };
    });
}

// Get all available categories
export function getAvailableCategories(): string[] {
  const categories = new Set<string>();
  allWords.forEach((word) => categories.add(word.category));
  return Array.from(categories);
}

// Get all available languages from word data
export function getAvailableLanguages(): string[] {
  const languages = new Set<string>();
  allWords.forEach((word) => {
    Object.keys(word.translations).forEach((lang) => languages.add(lang));
  });
  return Array.from(languages);
}
